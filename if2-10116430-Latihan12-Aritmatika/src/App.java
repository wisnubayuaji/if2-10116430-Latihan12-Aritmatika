public class App {
    public static void main(String[] args) throws Exception {
        int a = 10;
        int b = 20;
        int c = 25;
        int d = 25;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (b / a));
        System.out.println("a % b = " + (b % a));
        System.out.println("a % b = " + (c % a));
        System.out.println("a++ = " + (a++));
        System.out.println("b-- = " + (a--));
        System.out.println("d++ = " + (d++));
        System.out.println("++d = " + (++d));
    }
}
